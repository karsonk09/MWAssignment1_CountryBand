<h3 class="beginnings">Beginnings</h3>

<p>Boots 'N Beer first came together unintentionally on stage for karaoke in 
small bar in Des Moines. After performing, they found that they matched fairly well and had the 
makings of an actual band. Starting with small gigs, they began making headway with Boots 'N Beer 
when a scout heard some of their music and got them spots opening for more popular bands performing 
in the Des Moines area.</p>

<p class="beginnings">After meeting in a small bar in Des Moines, the band members discovered that 
they're band's heritage stretches all the way back into the old West. Joe Jack's 
great great grandfather, Jimmy Jack, was the sheriff of a small town just north 
of Texas, called Bootsinburg. His deputy and right hand man, Russell Raccoon, 
happens to be Randy Raccoon's great great grandfather. It may seem like a coincidence, 
but the members seem to think it is destiny. In Bootsinburg, the local community 
stable owner was named Danny Denim. You guessed it, Danny Denim was the great 
great grandfather of the one and only Denver Denim. Jake Grit's family has been 
a part of Bootsinburg's history for generation after generation as saloon owners, 
barbers, even bandits!</p>

